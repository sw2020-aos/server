package com.aos.requestorder;

import java.io.Serializable;

public class RequestOrderInfo implements Serializable {
	private String orderDate;
	private String supplierName;
	private String ingredientName;
	private String orderAmount;
	private String status;
	private String unitPrice;
	private String orderPrice;
	private String zipCode;
	private String address;
	private String detailAddress;
	private String email;
	private String phoneNo;

	public RequestOrderInfo() {

	}

	public RequestOrderInfo(String orderDate, String supplierName, String ingredientName, String orderAmount,
			String status, String unitPrice, String orderPrice, String zipCode, String address, String detailAddress,
			String email, String phoneNo) {
		super();
		this.orderDate = orderDate;
		this.supplierName = supplierName;
		this.ingredientName = ingredientName;
		this.orderAmount = orderAmount;
		this.status = status;
		this.unitPrice = unitPrice;
		this.orderPrice = orderPrice;
		this.zipCode = zipCode;
		this.address = address;
		this.detailAddress = detailAddress;
		this.email = email;
		this.phoneNo = phoneNo;
	}

	@Override
	public String toString() {
		return "RequestOrderInfo [orderDate=" + orderDate + ", supplierName=" + supplierName + ", ingredientName="
				+ ingredientName + ", orderAmount=" + orderAmount + ", status=" + status + ", unitPrice=" + unitPrice
				+ ", orderPrice=" + orderPrice + ", zipCode=" + zipCode + ", address=" + address + ", detailAddress="
				+ detailAddress + ", email=" + email + ", phoneNo=" + phoneNo + "]";
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getUnitPrice() {
		return unitPrice;
	}

	public void setUnitPrice(String unitPrice) {
		this.unitPrice = unitPrice;
	}

	public String getPhoneNo() {
		return phoneNo;
	}

	public void setPhoneNo(String phoneNo) {
		this.phoneNo = phoneNo;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}

	public String getZipCode() {
		return zipCode;
	}

	public void setZipCode(String zipCode) {
		this.zipCode = zipCode;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getDetailAddress() {
		return detailAddress;
	}

	public void setDetailAddress(String detailAddress) {
		this.detailAddress = detailAddress;
	}

	public String getOrderDate() {
		return orderDate;
	}

	public void setOrderDate(String orderDate) {
		this.orderDate = orderDate;
	}

	public String getSupplierName() {
		return supplierName;
	}

	public void setSupplierName(String supplierName) {
		this.supplierName = supplierName;
	}

	public String getIngredientName() {
		return ingredientName;
	}

	public void setIngredientName(String ingredientName) {
		this.ingredientName = ingredientName;
	}

	public String getOrderAmount() {
		return orderAmount;
	}

	public void setOrderAmount(String orderAmount) {
		this.orderAmount = orderAmount;
	}

	public String getOrderPrice() {
		return orderPrice;
	}

	public void setOrderPrice(String orderPrice) {
		this.orderPrice = orderPrice;
	}

}
